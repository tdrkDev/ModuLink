with open("sprites.cpp", "w") as f:
    with open("sprites.hpp", "w") as fh:
        fh.write("#include <LEDMatrixDriver.hpp>\n#pragma once\n\nextern LEDMatrixDriver matrix;\n\n")
        f.write('#include "sprites.hpp"\n\n')
        for sprite in SPRITES.keys():
            fh.write(f"void show_{sprite.lower()}();\n")
            f.write(f"void show_{sprite.lower()}() " + "{\n")
            f.write("\tmatrix.clear();\n")
            s = SPRITES[sprite]
            for i in range(0, len(s)):
                v = s[i]
                if v == 0:
                    continue

                x = i % 8
                y = int(i / 8)
                f.write(f"\tmatrix.setPixel({x}, {y}, true);\n")
            f.write("\tmatrix.display();\n")
            f.write("}\n\n")
            
